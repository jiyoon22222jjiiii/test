package day01;

public class PrintTest {
		//Ctrl + Spacebar : 자동완성
		//main 작성 후 자동완성
	public static void main(String[] args) {
		System.out.print(22);
		System.out.println("\n정다솔\n");
		
		// \n은 엔터의 의미입니다.
		System.out.println("\\n은 엔터의 의미입니다.\n");
		
		//"코리아 아이티 아카데미"
		System.out.println("\"코리아 아이디 아카데미\"");
		
		System.out.println('A');
		
		//syso 작성 후 자동완성 : println() 자동완성
		//수식은 결과값 하나로 봐야한다. (수식은 값이다!)
		System.out.println(3+5);
		
		//어떤 값이든 문자열과 + 를 하게  되면 연결이 일어난다.
		System.out.println(22+"년");
		
		//1 + 1 =2
		
		System.out.println(1 + " + " + 1 + " = " + 2);
//		System.out.println("1 + 1 = 2");
		System.out.printf("%d + %d = %d\n",1,1,2);
	}

}
